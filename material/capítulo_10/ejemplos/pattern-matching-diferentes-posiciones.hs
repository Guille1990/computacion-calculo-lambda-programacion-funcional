f :: ([Int], (Int, Int), (Int, Int), Int) -> Int
    
f ([1,1,1], (1,2), (3, 4), 5) = 1
f ([2,2,2], (1,2), (3, 4), 5) = 2
f (_, (0,0), _, _) = 3
    
main = do 
    print $ f ([1, 1, 1], (1, 2), (3, 4), 5) -- 1
    print $ f ([2, 2, 2], (1, 2), (3, 4), 5) -- 2
print $ f ([1, 2, 3], (0, 0), (5, 6), 10) -- 3
